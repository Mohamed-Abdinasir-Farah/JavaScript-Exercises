let isSunny = true;
let isWeekend = true;

console.log(isSunny && isWeekend);    // Outputs true
console.log(isSunny || isWeekend);    // Outputs true  
console.log(!isSunny);                // Outputs false
console.log(!isWeekend);             // Outputs false
console.log(!isSunny && !isWeekend); // Outputs false
console.log(!isSunny || isWeekend);  // Outputs  true